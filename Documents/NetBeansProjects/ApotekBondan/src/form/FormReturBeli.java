    public void tampilkandata() {
        DefaultTableModel tabelmapel = new DefaultTableModel();
        tabelmapel.addColumn("Kode Supplier");
        tabelmapel.addColumn("Nama Supplier");
        tabelmapel.addColumn("Kode Obat");
        tabelmapel.addColumn("Nama Obat");
         tabelmapel.addColumn("Harga Beli");
        tabelmapel.addColumn("Jumlah");       
        
        
        
        try {
            koneksi();
            String sql = "select returbeli.kodeSupplier, supplier.namaSupplier, returbeli.kodeObat, obat.namaObat, returbeli.harga, returbeli.qty from returbeli join supplier on returbeli.kodeSupplier=supplier.kodeSupplier join obat on returbeli.kodeObat=obat.kodeObat;";
            Statement stat = conn.createStatement();
            ResultSet res=stat.executeQuery(sql);
            while (res.next()) {
                tabelmapel.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),
                res.getString(4),res.getString(5),res.getString(6)});
            }
           jTable2.setModel(tabelmapel);
        } catch (Exception e) {
            System.out.println(e);
        }

    }
    
    

