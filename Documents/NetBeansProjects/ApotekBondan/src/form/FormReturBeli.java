    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
    if((txHarga.getText().length()==0)||(txJumlah.getText().length()==0)){
        JOptionPane.showMessageDialog(null, "Kolom Harus Di isi Semua", "Warning!!", JOptionPane.WARNING_MESSAGE);
    }
        try {
            Connection konek=Koneksi.Koneksi.getKoneksi();
            String query="insert into returbeli values(?, ?, ?, ?)";
            PreparedStatement state=konek.prepareStatement(query);
            state.setString(1, this.getKodeSupplier());
            state.setString(2, this.getKodeObat());
            state.setString(3, txHarga.getText());
            state.setString(4, txJumlah.getText());
            
            state.executeUpdate();
            System.out.println("Data disimpan");
            state.close();
            tampilkandata();
        } catch (SQLException ex) {
            System.out.println(ex);
            
        }finally{
            txHarga.setText("");
            txJumlah.setText("");
            
        }
    }//GEN-LAST:event_jButton1MouseClicked

